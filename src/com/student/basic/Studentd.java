package com.student.basic;

public class Studentd {
	private int id ;
	private String name ;
	private double fees;
	
	public void setId(int id) {
		this.id = id;
	}
    public void setName(String name) {
    	this.name = name;
    }
    public void setFees(double fees) {
    	this.fees = fees;
    }
    public int getId() {
    	return this.id;
    }
    public String getName() {
    	return this.name;	
    }
    public double getFees() {
    	return this.fees;
    }
}
